# Concatenacion

print("conc" + "atenacion")

# Tipos - Type

a = 10 #Variable a tipo interger

print(type(a))

b = 10.5 #Variable b tipo float

print(type(b))

# Numeros - Number

#Interger
print("100")

#Float
print("10.0")

# Booleanos - Boolean

verdad = True #Verdad

falso = False #Falso

print(type(verdad))
print(type(falso))

# Listas - List

entero = [10, 20, 30, 40] #Lista interger
flotante = [10.1, 20.2, 30.3, 40.4] #Lista float
cadena = ["text", "text1", "text2", "text3"] #Lista String
booleano = [True, False, True, False] #Lista Boolean

print(entero, flotante, cadena, booleano)

# Tuplas - Tuples

a = (10, 20, 30, 40) #tulples interger
b = (10.1, 20.2, 30.3, 40.4) #tuples float
c = ("text0", "text1", "text2", "text3") #tuples string
d = (True, False, True, False) #tuples Boolean

print(a, b, c, d)

# Diccionarios - Dictionary

